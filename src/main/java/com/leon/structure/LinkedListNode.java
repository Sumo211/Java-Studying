package com.leon.structure;

public class LinkedListNode {

    private int value;

    private LinkedListNode next;

    public int getValue() {
        return value;
    }

    public LinkedListNode getNext() {
        return next;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public void setNext(LinkedListNode next) {
        this.next = next;
    }

}
